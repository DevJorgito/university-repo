    private String imprimirPago() {
        StringBuilder contenido = new StringBuilder();
        contenido.append("Cliente: ").append(txtcliente.getText()).append("\n");
        contenido.append("Costo Total: ").append(lblCostoTotal.getText()).append("\n");
        contenido.append("Tipo de Comprobante: ").append(cbotipo_comprobante.getSelectedItem().toString()).append("\n");
        contenido.append("Número de Comprobante: ").append(txtnum_comprobante.getText()).append("\n");
        if (cbotipo_comprobante.getSelectedItem().equals("Factura")) {
            contenido.append("RUC: ").append(txtruc.getText()).append("\n");
        }
        contenido.append("IGV: ").append(txtigv.getText()).append("\n");
        contenido.append("Total Consumos: ").append(lblTotalConsumos.getText()).append("\n");
        contenido.append("Fecha de Emisión: ").append(new SimpleDateFormat("dd/MM/yyyy").format(datFechaEmision.getDate())).append("\n");
        contenido.append("Fecha de Pago: ").append(new SimpleDateFormat("dd/MM/yyyy").format(datFechaPago.getDate())).append("\n");
        contenido.append("Total a Pagar: ").append(txtTotalPago.getText()).append("\n");

        return contenido.toString();
    }

