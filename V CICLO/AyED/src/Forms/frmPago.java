    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        try {
            int idReserva = Integer.parseInt(txtidreserva.getText());
            Reserva reserva = DaoR.obtenerReservaPorId(idReserva);

            if (reserva == null) {
                JOptionPane.showMessageDialog(this, "Reserva no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (reserva.getEstadoR().contains("Pagado")) {
                JOptionPane.showMessageDialog(this, "La reserva ya está pagada", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (JOptionPane.showConfirmDialog(rootPane, "¿Se realizó el pago?", "Agregar Pago", JOptionPane.INFORMATION_MESSAGE, JOptionPane.YES_NO_OPTION) != JOptionPane.YES_OPTION) {
                return;
            }

            String tipoComprobante = cbotipo_comprobante.getSelectedItem().toString();
            String numComprobante = txtnum_comprobante.getText();
            BigDecimal igv = new BigDecimal(txtigv.getText());
            BigDecimal totalPago = new BigDecimal(txtTotalPago.getText());
            LocalDate fechaEmision = datFechaEmision.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            LocalDate fechaPago = datFechaPago.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            Date fechaEmisionDate = Date.from(fechaEmision.atStartOfDay(ZoneId.systemDefault()).toInstant());
            Date fechaPagoDate = Date.from(fechaPago.atStartOfDay(ZoneId.systemDefault()).toInstant());

            Pago pago = new Pago(0, idReserva, tipoComprobante, numComprobante, igv, totalPago, fechaEmisionDate, fechaPagoDate);

            DAO.Guardar(pago);

            // Imprimir el pago
            String contenido = imprimirPago();
            String nombreArchivo = "Boleta_" + txtnum_comprobante.getText() + "_" + System.currentTimeMillis() + ".txt";
            FileUtils.escribirPagoEnArchivo(nombreArchivo, contenido);

            // Actualizar estado de la reserva a "Pagado"
            String nuevoEstado;
            if (reserva.getEstadoR().equals("Hospedaje")) {
                nuevoEstado = "Hospedaje-Pagado";
            } else if (reserva.getEstadoR().equals("Reservada")) {
                nuevoEstado = "Reservada-Pagado";
            } else {
                throw new IllegalStateException("Estado desconocido: " + reserva.getEstadoR());
            }

            DaoR.actualizarEstado(idReserva, nuevoEstado);

            JOptionPane.showMessageDialog(this, "Pago registrado e impreso correctamente como " + nombreArchivo);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error en el formato de los datos numéricos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Error en el formato de fecha: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar o imprimir el pago: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

