    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:
        try {
        // Obtener el idReserva del campo de texto
        int idReserva = Integer.parseInt(txtidreserva.getText().trim());

        // Obtener la reserva por id
        Reserva reserva = DaoR.obtenerReservaPorId(idReserva);

        if (reserva == null) {
            JOptionPane.showMessageDialog(this, "Reserva no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!reserva.getEstadoR().contains("Pagado")) {
            JOptionPane.showMessageDialog(this, "El estado de la reserva no es 'Pagado'.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Imprimir el pago
        String contenido = imprimirPago();
        String nombreArchivo = "Boleta_" + txtnum_comprobante.getText() + "_" + System.currentTimeMillis() + ".txt";
        FileUtils.escribirPagoEnArchivo(nombreArchivo, contenido);

        JOptionPane.showMessageDialog(this, "Pago impreso correctamente como " + nombreArchivo);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error en el formato de los datos numéricos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al imprimir el pago: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnImprimirActionPerformed

