    public void RecuperarDesdeBD() {
        LH = DAO.ListarHabitaciones();
        DTM = new DefaultTableModel(null, Cabecera);
        for (Habitacion H : LH) {
            DTM.addRow(H.Convertir());
        }
        tablaHabitacionH.setModel(DTM);

        Filtro = new TableRowSorter(DTM); 
        tablaHabitacionH.setRowSorter(Filtro);  

        Configuracion();
    }
    
    public void Configuracion() {
        //esconder la columna ID
        tablaHabitacionH.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaHabitacionH.getColumnModel().getColumn(0).setMinWidth(0);
        tablaHabitacionH.getColumnModel().getColumn(0).setPreferredWidth(0);
        
    }
    
    private void Filtrar() {
        try {
            String Texto = txtfiltrar.getText();
            Filtro.setRowFilter(RowFilter.regexFilter(Texto));
        } catch (Exception e) {
        }
    }
    
