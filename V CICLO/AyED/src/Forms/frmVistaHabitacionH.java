    private void tablaHabitacionHMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaHabitacionHMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int fila= tablaHabitacionH.getSelectedRow();
            String cod;
            String valor;
            
            cod=tablaHabitacionH.getValueAt(fila, 0).toString();
            valor=tablaHabitacionH.getValueAt(fila, 1).toString();
            
            frmHospedaje.txtidhabitacion.setText(cod);
            frmHospedaje.txtnumero.setText(valor);
            this.dispose();
            
        }
    }//GEN-LAST:event_tablaHabitacionHMousePressed

