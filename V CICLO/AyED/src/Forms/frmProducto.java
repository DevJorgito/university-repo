    private boolean Validar() {
        if (txtnombre.getText().isEmpty()) {
            txtnombre.requestFocus();
            return false;
        }
        if (txtdescripcion.getText().isEmpty()) {
            txtdescripcion.requestFocus();
            return false;
        }
        if (txtprecioventa.getText().isEmpty()) {
            txtprecioventa.requestFocus();
            return false;
        }
        try {
            new BigDecimal(txtprecioventa.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio de venta debe ser un número válido");
            txtprecioventa.requestFocus();
            return false;
        }
        return true;
    }


    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (Validar() == false) {
            JOptionPane.showMessageDialog(null, "Falta ingresar datos");
            return;
        }

        if (JOptionPane.showConfirmDialog(rootPane, "¿Realmente desea continuar?", "Agregar Cliente", JOptionPane.INFORMATION_MESSAGE, JOptionPane.YES_NO_OPTION) != JOptionPane.YES_OPTION) {
            return;
        }

        String nombre = txtnombre.getText();
        String descripcion = txtdescripcion.getText();
        BigDecimal precioVenta = new BigDecimal(txtprecioventa.getText());

        Producto producto = new Producto(ID_Recuperado, nombre, descripcion, precioVenta);

        DAO.Guardar(producto);
        JOptionPane.showMessageDialog(null, "Producto guardado correctamente");

        LimpiarControles();
        RecuperarDesdeBD();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void Filtrar() {
        try {
            String Texto = txtfiltrar.getText();
            Filtro.setRowFilter(RowFilter.regexFilter(Texto));
        } catch (Exception e) {
        }
    }


    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int Fila = tablaProductos.getSelectedRow();

        if (Fila < 0) {
            JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna fila", "Resultado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (JOptionPane.showConfirmDialog(rootPane, "Se eliminará el registro, ¿desea continuar?",
                "Eliminar Registro", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) != JOptionPane.YES_OPTION) {
            return;
        }

        int ID = Integer.parseInt(tablaProductos.getValueAt(Fila, 0).toString());
        DAO.Eliminar(ID);
        JOptionPane.showMessageDialog(null, "Producto eliminado correctamente", "Resultado", JOptionPane.INFORMATION_MESSAGE);
        RecuperarDesdeBD();

    }//GEN-LAST:event_btnEliminarActionPerformed

