    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int Fila = tablaProductos.getSelectedRow();
        int columnaId = 0;

        if (Fila < 0) {
            JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna fila", "Resultado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int ID_Tabla = Integer.parseInt(tablaProductos.getValueAt(Fila, columnaId).toString());

        Producto producto = DAO.Leer(ID_Tabla);
        if (producto != null) {
            ID_Recuperado = producto.getIdProducto();

            txtnombre.setText(producto.getNombre());
            txtdescripcion.setText(producto.getDescripcion());
            txtprecioventa.setText(producto.getPrecioVenta().toString());
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el producto en la base de datos", "Resultado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        txtidproducto.setText(String.valueOf(ID_Recuperado));

    }//GEN-LAST:event_btnSeleccionarActionPerformed

